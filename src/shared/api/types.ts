/**
 * Контракты данных портфеля.
 * Эти типы — общий язык между слоем API (mock/БКС) и остальным приложением.
 * При подключении реального БКС форма ответа маппится в эти же типы.
 */

export type Currency = 'RUB' | 'USD'

/** Тип выплаты: купон, частичная амортизация номинала, полное погашение. */
export type PaymentType = 'coupon' | 'amortization' | 'redemption'

/** Одна выплата по облигации (на одну бумагу). */
export interface CouponPayment {
  /** ISO-дата выплаты, YYYY-MM-DD */
  date: string
  /** Сумма выплаты на одну облигацию, в валюте бумаги */
  amountPerUnit: number
  /** Тип выплаты (по умолчанию — купон) */
  type?: PaymentType
}

/** Облигация в портфеле. */
export interface Bond {
  id: string
  /** Название эмитента/выпуска */
  name: string
  /** Короткий код-бейдж эмитента (2–4 буквы) */
  short: string
  ticker: string
  currency: Currency
  /** Количество бумаг в портфеле */
  quantity: number
  /** Текущая котировка (цена за одну бумагу), в валюте */
  price: number
  /** Цена покупки за одну бумагу (для расчёта вложенного и прибыли) */
  purchasePrice: number
  /** Котировка на закрытие предыдущего дня (для дневного изменения) */
  previousPrice: number
  /** Номинал бумаги */
  faceValue: number
  /** Годовая купонная доходность к текущей цене, % */
  couponYield: number
  /** График купонных выплат на горизонте (на одну бумагу) */
  coupons: CouponPayment[]
  /**
   * Ближайшая эффективная дата погашения/оферты, ISO YYYY-MM-DD.
   * Для бумаг с put-опционом это дата оферты, для остальных — погашение.
   * Отдельного поля «оферта» справочник БКС не отдаёт (см. bcs/instruments.ts).
   */
  maturityDate?: string
  /** Годовая ставка купона, % от номинала (справочно) */
  couponRate?: number
  /** Тип купона: «Постоянный» | «Переменный» (справочно) */
  couponType?: string
}

/** Снимок портфеля: облигации + свободный рублёвый остаток. */
export interface PortfolioSnapshot {
  bonds: Bond[]
  /** Денежный остаток (свободные рубли на счёте) */
  cashRub: number
}

/** Источник данных портфеля (для UI-индикации). */
export type ApiSource = 'mock' | 'bcs'
