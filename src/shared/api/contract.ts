import type { PortfolioSnapshot } from './types'

/**
 * Единый контракт доступа к данным портфеля.
 * Реализуется мок-провайдером (сейчас) и провайдером БКС (позже).
 * Приложение зависит только от этого интерфейса, поэтому переключение
 * на реальный бэкенд не затрагивает UI, фичи и сущности.
 */
export interface PortfolioApi {
  /** Получить снимок портфеля: облигации + денежный остаток. */
  getPortfolio(): Promise<PortfolioSnapshot>

  /**
   * Загрузить демонстрационные данные (кнопка «Загрузить пример» из ТЗ §9).
   * Реальный провайдер может не поддерживать — тогда метод отсутствует.
   */
  loadExample?(): Promise<void>

  /** Сбросить данные к пустому состоянию. Опционально. */
  reset?(): Promise<void>
}
